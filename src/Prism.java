public class Prism {

    int x, y, z, width, height, length;

    Prism(int x, int y, int z, int width, int height, int length) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.width = width;
        this.height = height;
        this.length = length;
    }



}
